     */
    public function store(Request $request)
    {
        //Almacenar en la BD nuevos recursos
        $options=[
          'title'=>$request->title,
          'description'=>$request->description,
          'price'=>$request->price
        ];

        if(Product::create($options)){
          return redirect('/productos');
        }else{
          return view('products.create');
        }
    }

